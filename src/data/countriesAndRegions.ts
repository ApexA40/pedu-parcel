export const COUNTRY_CODES = Object.keys(COUNTRIES_AND_REGIONS).sort((a, b) => {
    const countryA = COUNTRIES_AND_REGIONS[a].name;
    const countryB = COUNTRIES_AND_REGIONS[b].name;
    return countryA.localeCompare(countryB);
});

export const getCountryName = (code: string): string => {
    return COUNTRIES_AND_REGIONS[code]?.name || code;
};

export const getRegionsByCountry = (countryCode: string): string[] => {
    return COUNTRIES_AND_REGIONS[countryCode]?.regions || [];
};
